package org.csi.yucca.dataservice.metadataapi.delegate.v01.i18n;

public class SubDomainElement {
	private String codDomain;
	private String codSubDomain;
	private String langIt;
	private String langEn;

	public String getCodDomain() {
		return codDomain;
	}

	public void setCodDomain(String codDomain) {
		this.codDomain = codDomain;
	}

	public String getCodSubDomain() {
		return codSubDomain;
	}

	public void setCodSubDomain(String codSubDomain) {
		this.codSubDomain = codSubDomain;
	}

	public String getLangIt() {
		return langIt;
	}

	public void setLangIt(String langIt) {
		this.langIt = langIt;
	}

	public String getLangEn() {
		return langEn;
	}

	public void setLangEn(String langEn) {
		this.langEn = langEn;
	}
}
