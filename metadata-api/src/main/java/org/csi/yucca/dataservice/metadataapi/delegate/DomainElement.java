package org.csi.yucca.dataservice.metadataapi.delegate;

public class DomainElement {
	private String codDomain;
	private String langIt;
	private String langEn;

	public String getCodDomain() {
		return codDomain;
	}

	public void setCodDomain(String codDomain) {
		this.codDomain = codDomain;
	}

	public String getLangIt() {
		return langIt;
	}

	public void setLangIt(String langIt) {
		this.langIt = langIt;
	}

	public String getLangEn() {
		return langEn;
	}

	public void setLangEn(String langEn) {
		this.langEn = langEn;
	}
}
